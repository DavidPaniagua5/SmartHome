//Funcion para convertir de hexadecimal a rgb
function convertirHexRGB(hex) {
  // Elimina el '#' si est√° presente
  const shorthandRegex = /^#?([a-f\d])([a-f\d])([a-f\d])$/i;
  hex = hex.replace(shorthandRegex, function(m, r, g, b) {
    return r + r + g + g + b + b;
  });

  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);

  // Si encuentra una coincidencia, devuelve el objeto RGB, de lo contrario, null
  return result ? {
    r: parseInt(result[1], 16),
    g: parseInt(result[2], 16),
    b: parseInt(result[3], 16)
  } : null;
}

export default convertirHexRGB